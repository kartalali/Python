love = "i love you Melis"

print(love.capitalize())  # Converts the first character to upper case

print(love.casefold())  # Converts string into lower case

print(love.center(150))  # Returns a centered string

print(love.count("i", 0, 16))  # Returns the number of times a specified value occurs in a string

